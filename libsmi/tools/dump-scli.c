static char*
translate(char *m)
{
    char *s;
    int i;

    s = xstrdup(m);
    for (i = 0; s[i]; i++) {
	if (s[i] == '-') s[i] = '_';
    }
  
    while (isKeyword(s)) {
	s = xrealloc(s, strlen(s) + 2);
	strcat(s, "_");
    }
    
    return s;
}



static char*
translateUpper(char *m)
{
    char *s;
    int i;

    s = xstrdup(m);
    for (i = 0; s[i]; i++) {
	if (s[i] == '-') s[i] = '_';
	if (islower((int) s[i])) {
	    s[i] = toupper(s[i]);
	}
    }
  
    while (isKeyword(s)) {
	s = xrealloc(s, strlen(s) + 2);
	strcat(s, "_");
    }
    
    return s;
}



